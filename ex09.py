#Faça um programa em Python capaz de ler uma temperatura em graus Celsius e convertê-la em Fahrenheit. A fórmula da conversão é: F = 32 + 9C/5

"""Converter temperatura (T) de graus Celsius (C) para Fahrenheit (F):

T(°F) = T(°C) × 9/5 + 32
Converter de graus Fahrenheit (F) para Celsius (C):

T(°C) = (T(°F) - 32) × 5/9 """





temperatura_c = float (input ( " Informe graus em celsius: "))

temperatura_f = (temperatura_c * (9/5) + 32)

print (temperatura_f)




 